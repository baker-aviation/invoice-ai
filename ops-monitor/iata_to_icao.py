def to_icao(code: str | None) -> str | None:
    """Convert IATA/FAA airport code to ICAO. US airports get K prefix."""
    if not code:
        return None
    upper = code.strip().upper()
    if len(upper) == 4:
        return upper  # Already ICAO
    if upper in IATA_TO_ICAO:
        return IATA_TO_ICAO[upper]
    # US domestic: 3-letter IATA → K + IATA
    if len(upper) == 3 and upper.isalpha():
        return "K" + upper
    return upper

