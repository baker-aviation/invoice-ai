  WHEN 'N533FX' THEN 'KOW533'
  WHEN 'N519FX' THEN 'KOW519'
  WHEN 'N541FX' THEN 'KOW541'
  WHEN 'N521FX' THEN 'KOW521'
  WHEN 'N554FX' THEN 'KOW554'
  WHEN 'N992MG' THEN 'KOW992'
  WHEN 'N988TX' THEN 'KOW988'
  WHEN 'N733FL' THEN 'KOW733'
  WHEN 'N883TR' THEN 'KOW883'
  WHEN 'N416F'  THEN 'KOW416'
  WHEN 'N954JS' THEN 'KOW954'
  WHEN 'N939TX' THEN 'KOW939'
  WHEN 'N998CX' THEN 'KOW998'
  WHEN 'N971JS' THEN 'KOW971'
  WHEN 'N51GB'  THEN 'KOW51'
  WHEN 'N513JB' THEN 'KOW513'
  WHEN 'N201HR' THEN 'KOW201'
  WHEN 'N910E'  THEN 'KOW910'
  WHEN 'N106PC' THEN 'KOW106'
  WHEN 'N860TX' THEN 'KOW860'
  WHEN 'N301HR' THEN 'KOW301'
  WHEN 'N102VR' THEN 'KOW102'
  WHEN 'N700LH' THEN 'KOW700'
  WHEN 'N957JS' THEN 'KOW957'
  WHEN 'N703TX' THEN 'KOW703'
  WHEN 'N187CR' THEN 'KOW187'
