-- Add callsign column to ics_sources for FlightAware LADD/PIA fallback lookups.
-- Baker Aviation files under Kalitta Charters (ICAO: KOW) callsigns.

ALTER TABLE ics_sources ADD COLUMN IF NOT EXISTS callsign text;

-- Seed callsigns for existing Baker fleet based on known KOW mappings.
-- These can be edited from the Super Admin dashboard.
