WHERE callsign IS NULL
  AND label IN (
    'N533FX','N519FX','N541FX','N521FX','N554FX','N992MG','N988TX','N733FL',
    'N883TR','N416F','N954JS','N939TX','N998CX','N971JS','N51GB','N513JB',
    'N201HR','N910E','N106PC','N860TX','N301HR','N102VR','N700LH','N957JS',
    'N703TX','N187CR','N125TH','N125DZ','N818CF','N955GH','N552FX','N553FX',
    'N371DB','N520FX'
